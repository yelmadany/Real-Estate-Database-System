    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:

        int n = JOptionPane.showOptionDialog(new JFrame(), "Are you sure you would like to continue?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, new Object[] {"Yes", "No"}, JOptionPane.YES_OPTION);
        if(n==JOptionPane.YES_OPTION)
        {
            try {
                if(ownermode){

                        String prepSQL = "delete owner WHERE owner_id = " + txtID.getText().trim();

                        int result = con.executePrepared(prepSQL);
                        if (result > 0) {

                            javax.swing.JLabel label = new javax.swing.JLabel("owner No " + txtID.getText() + " deleted successfully.");
                            label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                            JOptionPane.showMessageDialog(null, label, "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                            rs = con.executeStatement("SELECT owner_id, name,phone, password FROM owner where owner_id  != 300000001 ");
                            rs.first();
                            populateOwnerFields();

                        } }
                        if(rentermode){
 
                                String prepSQL = "delete renter  WHERE renter_id = " + txtID.getText().trim();

                                int result = con.executePrepared(prepSQL);
                                if (result > 0) {

                                    javax.swing.JLabel label = new javax.swing.JLabel("renter No " + txtID.getText() + " deleted successfully.");
                                    label.setFont(new java.awt.Font("Arial", java.awt.Font.BOLD, 18));
                                    JOptionPane.showMessageDialog(null, label, "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                                    rs = con.executeStatement("SELECT renter_id, name,phone, password FROM renter ");
                                    rs.first();
                                    populateRenterFields();

                                } 

                                }

                                else
                                {
                                    System.out.print("");  // to do nothing and close the JOptionPane
                                }
                            }
                            catch (SQLException e2) {

                                JOptionPane.showMessageDialog(null, "Error deleting employee." + e2.getMessage());

                            }
                        }

    }//GEN-LAST:event_btnUpdateActionPerformed

